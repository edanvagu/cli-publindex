import { readFileSync, readdirSync, statSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { join, relative, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, '..');
const distDir = join(root, 'extension', 'dist');
const outFile = join(root, 'src', 'cli', 'generated', 'extension-bundle.ts');

if (!existsSync(distDir)) {
  console.error(`[embed-extension] ${distDir} not found — run "npm run ext:build" first.`);
  process.exit(1);
}

function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    const st = statSync(full);
    if (st.isDirectory()) out.push(...walk(full));
    else out.push(full);
  }
  return out;
}

const files = walk(distDir)
  .filter((f) => !f.endsWith('.map'))
  .sort();

const entries = files
  .map((f) => {
    const rel = relative(distDir, f).replace(/\\/g, '/');
    const b64 = readFileSync(f).toString('base64');
    return `  ${JSON.stringify(rel)}: ${JSON.stringify(b64)},`;
  })
  .join('\n');

const content = `// Auto-generated by scripts/embed-extension.mjs — do not edit.
// Emitted at build time with the Chromium extension contents (base64) so the CLI binary can extract the extension to disk on demand without a network round-trip.
export const EXTENSION_FILES: Record<string, string> = {
${entries}
};

export const EXTENSION_FILE_COUNT: number = ${files.length};
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, content);
console.info(`[embed-extension] wrote ${files.length} files (${content.length.toLocaleString()} bytes) → ${outFile}`);
